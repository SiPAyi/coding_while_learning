#by default the range's first argument is set to 0
for num in range(10):
	print(num)

# we can add starting and ending points of the range
for number in range(10,20):
	print(number)

# we can also defined the step to range function
# but we can't use a decimal number as step
for float in range(1,6,2):
	print(float)
